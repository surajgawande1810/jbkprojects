////3. Why is Java platform independent?
//Answer:
//Java source code is compiled into bytecode, 
//which is executed by the Java Virtual Machine (JVM). 
//Since the JVM is available on different operating systems, the same bytecode can run on any platform.

//4. What is JVM?
//Answer:
//The Java Virtual Machine (JVM) is a virtual machine that executes Java bytecode and provides platform independence.

//5. What is JDK?
//Answer:
//The Java Development Kit (JDK) is a software package used to develop Java applications.
//It includes the JRE, compiler (javac), debugger, and development tools.

//6. What is JRE?
//Answer:
//The Java Runtime Environment (JRE) provides the libraries and JVM required to run Java applications. 
//It does not include development tools like the compiler.


